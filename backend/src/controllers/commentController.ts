import type { Request, Response } from "express";
import * as queries from "../db/queries";
import { getAuth } from "@clerk/express";

export const createComment = async (req: Request, res: Response ) => {

    try {
        const {userId} = getAuth(req);
        if(!userId){
            return res.status(401).send({error: "Unauthorized"});
        }

        const {productId} = req.params;
        const {content} = req.body;

        if(!content){
            return res.status(400).send({error: "Comment content is required"});
        }

        const product = await queries.getProductById(productId);
        if(!product){
            return res.status(404).send({error: "Product not found"});
        }

        const comment = await queries.createComment({
            content,
            userId,
            productId,
        })

        res.status(201).json(comment);
    }catch (error) {
        console.error("Error creating comment", error);
        res.status(500).send({error: "Failed to create comment"});
    }
}

export const deleteComment = async (req: Request, res: Response ) => {

    try {
        const {userId} = getAuth(req);
        if(!userId){
            return res.status(401).send({error: "Unauthorized"});
        }

        const {commentId} = req.params;

        const existingComment = await queries.getCommentById(commentId);
        if(!existingComment){
            return res.status(404).send({error: "Could not find comment"});
        }

        if(existingComment.userId !== userId){
            return res.status(403).send({error: "You can only delete your own comments"});
        }

        await queries.deleteComment(commentId);
        res.status(200).json({message: "Comment deleted successfully"});


    }catch (error) {
        console.error("Error deleting comment", error);
        res.status(500).send({error: "Failed to delete comment"});
    }
}